// 从机场名称提取城市名称的函数
export function extractCityFromAirport(airport: string): string {
  // 首先尝试从映射表中查找
  if (AIRPORT_CITY_MAP[airport]) {
    return AIRPORT_CITY_MAP[airport];
  }
  
  // 尝试通过规则提取
  let cityName = airport;
  
  // 先处理被截断的机场名称（如"鄂尔多斯伊金霍洛国际机"）
  if (cityName.endsWith('国际机') && !cityName.endsWith('国际机场')) {
    cityName = cityName.slice(0, -3); // 移除"国际机"
  }
  
  // 移除常见的机场后缀
  const suffixes = ['国际机场', '机场', '国际'];
  for (const suffix of suffixes) {
    if (cityName.endsWith(suffix)) {
      cityName = cityName.slice(0, -suffix.length);
      break; // 只移除一个后缀
    }
  }
  
  // 处理特殊的机场名称格式
  // 1. "鄂尔多斯伊金霍洛" -> "鄂尔多斯"
  if (cityName.includes('伊金霍洛')) {
    return '鄂尔多斯';
  }
  
  // 2. "呼伦贝尔海拉尔" -> "呼伦贝尔"
  if (cityName.includes('海拉尔')) {
    return '呼伦贝尔';
  }
  
  // 3. "扬州泰州" -> "扬州"（共用机场）
  if (cityName === '扬州泰州') {
    return '扬州';
  }
  
  // 4. "揭阳潮汕" -> "揭阳"
  if (cityName === '揭阳潮汕') {
    return '揭阳';
  }
  
  // 5. "鄂州花湖" -> "鄂州"
  if (cityName === '鄂州花湖') {
    return '鄂州';
  }
  
  // 对于一些特殊格式，尝试提取城市名
  const specialPatterns = [
    // 先尝试匹配2-4个汉字的城市名
    { pattern: /^([\u4e00-\u9fa5]{2,4})(?:[\u4e00-\u9fa5]{2,})/, extract: 1 },
    // 再尝试匹配带行政后缀的（注意：这可能误匹配"鄂州"为"鄂"）
    { pattern: /^(.{2,})(?:市|盟|地区)/, extract: 1 }  // 至少2个字符，不匹配单独的"州"
  ];
  
  for (const { pattern, extract } of specialPatterns) {
    const match = cityName.match(pattern);
    if (match && match[extract]) {
      return match[extract];
    }
  }
  
  // 如果城市名太长（超过4个字），可能是复合名称，取前面部分
  if (cityName.length > 4) {
    // 检查是否包含已知的城市名称模式
    const knownCityPatterns = ['林芝', '阿里', '日喀则', '西双版纳', '阿拉善左旗', '阿拉善右旗'];
    for (const pattern of knownCityPatterns) {
      if (cityName.startsWith(pattern)) {
        return pattern;
      }
    }
    
    // 否则返回前3个字
    return cityName.slice(0, 3);
  }
  
  // 返回处理后的名称
  return cityName;
}